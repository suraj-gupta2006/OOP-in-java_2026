public class TypeCasting {
    public static void main(String[] args) {
        // Implicit casting (widening)
        int intVal = 100;
        double doubleVal = intVal; // automatic
        System.out.println("Implicit: int " + intVal + " to double " + doubleVal);

        // Explicit casting (narrowing)
        double pi = 8.99;
        int truncated = (int) pi;
        System.out.println("Explicit: double " + pi + " to int " + truncated);
    System.out.println("suraj gupta");
    }
}